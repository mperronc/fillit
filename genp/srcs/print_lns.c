#include "../incl/generator.h"

void	print_lns(char *s)
{
	int i = 0;

	while (*s)
	{
		write(1, s, 1);
		s++;
		i++;
		if (i % 4 == 0)
		{
			write(1, "\n", 1);
			i = 0;
		}
	}
	write(1, "\n", 1);
}
